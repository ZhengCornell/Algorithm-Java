package hashmap;

public class Ren {
	private String name;
	private String money;
	public Ren(String name, String money) {
		super();
		this.name = name;
		this.money = money;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public Ren(){
		
	}
}
